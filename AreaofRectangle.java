public class AreaofRectangle {
    public static void main(String[] args) {
        int length = 5;
        int width = 6;
        int area;

        area = length * width;

        System.out.println("The length is : " + length);
        System.out.println("The width is : " + width);

        System.out.println("The Area of Rectangle is : " + area);
    }
    
}
